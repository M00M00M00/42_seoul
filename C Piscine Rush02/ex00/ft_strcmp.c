int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	c;

	c = 0;
	i = 0;
	if (s1[i] == '\0' && s2[i] == '\0')
	{
		c = 0;
	}
	else
	{
		while (s1[i] == s2[i])
		{
			if (s1[i] == '\0' || s2[i] == '\0')
				break ;
			i++;
		}
		c = s1[i] - s2[i];
	}
	return (c);
}
