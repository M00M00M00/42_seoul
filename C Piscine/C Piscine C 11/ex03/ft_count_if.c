int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	char	**s;
	int		i;
	int		cnt;

	s = tab;
	i = 0;
	cnt = 0;
	while (i < length)
	{
		if (f(s[i]) != 0)
			cnt++;
		i++;
	}
	return (cnt);
}
